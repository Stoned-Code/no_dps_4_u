

def list_to_tuple(li: list):
    """
    Converts a list to a tuple.

    Arguments:
    ----------
    li: `list`
        The list you want to turn into a tuple.
    """
    return tuple(li)


def tuple_to_list(tup: tuple):
    """
    Converts a tuple to a list.

    Arguments:
    ----------
    tup: `tuple`
        The tuple you want to turn into a list.
    """
    return list(tup)